/**
 * #Text
 */
new Vue({
    el: '#parte1',
    data:{
        parte1: 'Holi es parte 1'
    }
})
 /**
 * #Raw HTML
 */
new Vue({
    el: '#parte2',
    data:{
        parte2: '<span style="color:red;">Holi es parte 2 con color</span>'
    }
})
 /**
 * #Attributes
 */

new Vue({
    el: '#parte3',
    data:{
        parte3: true
    }
})
/**
 * #Using Javascript expressions
 */
new Vue({
    el: '#parte4',
    data: {
        parte4:5
    }
})

